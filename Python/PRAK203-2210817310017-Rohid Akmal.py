a = int(input("Masukkan Nilai a     : "))
b = int(input("Masukkan Nilai b     : "))
i = int(input("Masukkan Nilai i     : "))
j = int(input("Masukkan Nilai j     : "))
x = int(input("Masukkan Nilai x     : "))
y = int(input("Masukkan Nilai y     : "))

hsl = (a - b) * (i / j) - (x + y)
print("Hasil : %.3f "%(hsl))